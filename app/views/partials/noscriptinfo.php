<div id="noscript-description">
    <div id="noscript" style="text-align: center; color: #a53221; font-size: 40px">Jak włączyć obsługę JavaScript w
        przeglądarce?
    </div>
    <div>
        <h1 style="text-align: left; color: #a53221; font-size: 20px">Google Chrome</h1>
        <p>
        <ul>
            <li>Kliknij ikonę menu przeglądarki Chrome na pasku narzędzi przeglądarki.</li>
            <li>Wybierz Ustawienia.</li>
            <li>Na stronie Ustawienia kliknij link Pokaż ustawienia zaawansowane...</li>
            <li>Kliknij ikonę menu przeglądarki Chrome na pasku narzędzi przeglądarki.</li>
            <li>W sekcji Prywatność kliknij Ustawienia treści.</li>
            <li>W sekcji JavaScript wybierz Zezwalaj na wykonywanie kodu JavaScript w witrynach (zalecane).</li>
            <li>Kliknij Gotowe.</li>

        </ul>
        </p>
        <h1 style="text-align: left; color: #a53221; font-size: 20px">Mozilla Firefox</h1>
        <p>
        <ul>
            <li>Kliknij menu Narzędzia.</li>
            <li>Kliknij polecenie Opcje.</li>
            <li>Na lewym pasku nawigacyjnym wybierz pozycję Przeglądanie.</li>
            <li>Zaznacz pole wyboru Włącz obsługę języka JavaScript i kliknij przycisk OK.</li>
        </ul>
        </p>
        <h1 style="text-align: left; color: #a53221; font-size: 20px">Apple Safari</h1>
        <p>
        <ul>
            <li>Kliknij menu Safari.</li>
            <li>Kliknij polecenie Preferences (Preferencje).</li>
            <li>Kliknij pozycję Security (Zabezpieczenia).</li>
            <li>Zaznacz pole wyboru Enable JavaScript (Włącz JavaScript).</li>
        </ul>
        </p>
    </div>

</div>